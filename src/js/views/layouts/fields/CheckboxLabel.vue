<template>
    <label
        :class="getHtmlClass('label')" data-toggle="tooltip" data-placement="top" :title="label">
        <i-base-checkbox :transType="transType" v-bind="$props" @change="$emit('change', $event )"></i-base-checkbox>
        <span :class="classForCheckmark"></span>
    </label>
</template>


<script>

    import GlobalField from "../../../handel/functions/field/global.func";

    export default {
        name: 'i-checkbox-label',
        index: {
            group: 'fields',
            html: 'checkbox-label',
        },
        model: {
            event: 'change'
        },
        props: {
            label: String,
            placeholder: String,
            value: [String, Number, Object, Array],
            desc: [String, Number, Object, Array],
            storeNamespace: {
                type: [String, Object],
                default: 'DataSingle'
            },
            transType: {
                type: [String, Object],
                default: 'current'
            },
            fieldIndex: [String, Object, Function],
            slots: Object,
            options: {
                type: [Object, Array, Function],
                default: () => {
                    return {
                        field: {
                            control_field: false
                        }
                    }
                }
            },
            css: {
                type: [Object, Array],
                default: () => []
            },
        },
        computed: {
            ...GlobalField.computed(),
            classForCheckmark() {
                return this.classForField + " " + this.getOption('color', 'color-primary');
            },
        },
        methods: {
            ...GlobalField.methods()
        }
    }

</script>
